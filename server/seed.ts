async function removeCopyScenarioIfPresent() {
  const copy = await getScenarioByTitle("Sports Injury - Primary Assessment (Copy)");
  if (copy) {
    log(`Removing legacy scenario: ${copy.title}`, "seed");
    await deleteScenarioData(copy.id);
  }
}

async function ensureScenarioCovers() {
  const allScenarios = await db.select().from(scenarios);
  let updated = 0;
  for (const s of allScenarios) {
    const desired = SCENARIO_COVERS[s.title];
    if (desired && s.imageUrl !== desired) {
      await db.update(scenarios).set({ imageUrl: desired }).where(eq(scenarios.id, s.id));
      updated += 1;
    }
  }
  if (updated > 0) {
    log(`Updated cover images for ${updated} scenario(s)`, "seed");
  }
}

export async function seedDatabase() {
  let seededCount = 0;

  const existingS1 = await getScenarioByTitle("Sports Injury - Primary Assessment");
  if (existingS1) {
    const stepCount = await getStepCount(existingS1.id);
    if (stepCount !== 7) {
      log(`Scenario 1 has ${stepCount} steps (expected 7), re-seeding Scenario 1...`, "seed");
      await deleteScenarioData(existingS1.id);
      await seedScenario1Only();
      seededCount += 1;
    }
  } else {
    log("Seeding EMS scenarios...", "seed");
    await seedEMSScenarios();
    seededCount += 4;
  }

  if (!(await scenarioExists("Acute Stroke - CVA Recognition"))) {
    log("Seeding Nursing scenarios...", "seed");
    await seedNursingScenario();
    seededCount += 1;
  }

  const existingS3 = await getScenarioByTitle("Respiratory Failure - Elderly Patient");
  if (existingS3) {
    const stepCount = await getStepCount(existingS3.id);
    if (stepCount !== 8) {
      log(`Scenario 3 has ${stepCount} steps (expected 8), re-seeding Scenario 3 (BVM / Geriatric Respiratory Failure)...`, "seed");
      await deleteScenarioData(existingS3.id);
      await createScenario3WithSteps();
      seededCount += 1;
    }
  } else {
    log("Seeding Scenario 3 (BVM / Geriatric Respiratory Failure)...", "seed");
    await createScenario3WithSteps();
    seededCount += 1;
  }

  if (!(await scenarioExists("Severe Hemorrhage - Thigh Laceration"))) {
    log("Seeding Scenario 7 (Hemorrhage)...", "seed");
    await createScenario7WithSteps();
    seededCount += 1;
  }

  if (!(await scenarioExists("Combative Overdose - Suspected Opioid Reversal"))) {
    log("Seeding Scenario 8 (Combative Overdose)...", "seed");
    await createScenario8WithSteps();
    seededCount += 1;
  }

  if (!(await scenarioExists("Pediatric Asthma Attack - Acute Exacerbation"))) {
    log("Seeding Scenario 9 (Pediatric Asthma)...", "seed");
    await createScenario9WithSteps();
    seededCount += 1;
  }

  if (!(await scenarioExists("Multi-Patient MVC - Driver #1 (Post-Triage)"))) {
    log("Seeding Scenario 10 (Multi-Patient MVC - Driver #1)...", "seed");
    await createScenario10WithSteps();
    seededCount += 1;
  }

  if (!(await scenarioExists("Elderly Fall - Possible Head Injury (Anticoagulated)"))) {
    log("Seeding Scenario 11 (Elderly Fall - Anticoagulated Head Injury)...", "seed");
    await createScenario11WithSteps();
    seededCount += 1;
  }

  if (!(await scenarioExists("Tension Pneumothorax - Industrial Chest Trauma"))) {
    log("Seeding Scenario 12 (Tension Pneumothorax - Industrial Trauma)...", "seed");
    await createScenario12WithSteps();
    seededCount += 1;
  }

  if (seededCount === 0) {
    log("Database already seeded", "seed");
  } else {
    log(`Seeded ${seededCount} new scenarios`, "seed");
  }

  await removeCopyScenarioIfPresent();
  await ensureScenarioCovers();
}

